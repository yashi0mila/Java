public class exercicio1_laco{

    public static void main(String[] args){

        //Criação de variáveis
        int numeros;
        
        //Inicialização das variáveis
        numeros=0;

        while(numeros<=1000){
            System.out.println("Número: "+ numeros);
            numeros++;
        }
    }
}

